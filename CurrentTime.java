public class CurrentTime {
int time;
    public CurrentTime(int time) {


        {
            if (time == 8) {
                System.out.println("Поднять шторы");
            } else if (time == 11) {
                System.out.println("Поднять шторы");
            } else if (time == 20) {
                System.out.println("Включить чайник");

                if (time >= 20) {
                    System.out.println("Опустить шторы");
                }
            }
        }
    }
}